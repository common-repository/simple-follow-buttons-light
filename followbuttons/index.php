<script src="https://ajax.googleapis.com/ajax/libs/jquery/2.1.3/jquery.min.js"></script>
<script>
	$.fn.removeClassPrefix = function(prefix) {
		this.each(function(i, el) {
			var classes = el.className.split(' ').filter(function(c) {
				return c.lastIndexOf(prefix, 0) !== 0;
			});
			el.className = $.trim(classes.join(' '));
		});
		return this;
	};

	$(function(){
		var currTheme = parseInt(localStorage.getItem('themeNo'));

		if ( typeof currTheme !== 'number' ) {
			currTheme = 0;
		}

		$('#style>select').on('change', function() {
			var themeNo = $(this)[0].selectedIndex+1;
			$('.sfbl-wrap').removeClassPrefix('sfbl--theme').addClass('sfbl--theme-'+themeNo);
			$('.theme-link').attr('href', 'assets/css/themes/'+themeNo+'.css');
			localStorage.setItem('themeNo', themeNo);
		});

		$('.sfbl-toggle-switch').on('click', function() {
			$(this).parent().toggleClass('sfbl--state-hidden');
		});

		$('#style>select').val(currTheme).trigger('change');
	});
</script>
